							 <h6 class="mb-4"><strong>Business environment – SMes are critical to the global economy but account for little cross-border trade</strong></h6>
							 
							    <li>
							     SMes worldwide number between 420 million and 510 million. They account for about 95% of all companies, around 50% of global gross domestic product and about 70% of job creation yet account for only roughly 20% of cross-border trade, while large global companies generate about 80%
								</li>
								
								<h6 class="mb-1 mt-3"><strong>Challenges- fostering confidence, trust and trade by developing credit history tools</strong></h6>
								
								 <li>Numerous barriers, including geography, language, culture, scale and unfamiliar business practices obstruct cross-border trade between SME buyers and sellers.	</li>	
								
								 <li>Risk- averse SMEs face a knowledge vacuum which undermines trust. </li>		
								
								 <li>Transaction security is the No. 1 concern of SMeS; other concerns include a lack of confidence in the technology, lack of affordable and quality infrastructure, a shortage of skills/human capital, service trade barriers, high costs and poor access to financing.
								</li>
                        
						<h6 class="mb-4"><strong>Trade Assurance- ATZ Cart ’s free, data-driven trade facilitation service </strong></h6>
								
								 <li>
								Trade Assurance, launched in May 2015, combines One Touch business processing know-how with ATZ Cart .com data analytics.
								</li>
								 <li>
								Trade Assurance applies a credit-rating system that incentivizes suppliers to produce and deliver high-quality goods on time and according to their buyers’ specifications.
								</li>
								
								<li>
								Credit scoring helps SMEs to connect, build trust and gain easier access to financial services.
								</li>								
								<li>
								Trade Assurance covers and protects the buyers’ intellectual property.
								</li>							
								
								<li>
								One Touch provides business processing services to help SMeS with cross-border trade, including custom clearance, goods inspection, tax refund, logistics, payments, etc.
								</li>
								
								<h6 class="mb-1 mt-3"><strong>Finding credible suppliers  </strong></h6>
								
								
								<li>ATZ Cart .com research shows that what motivates buyers to source and purchase online is that what motivates buyers to source and purchase online is that they value having a good variety of products to choose from. Of course, they look for product quality and low pricing too. However, a secure and convenient payment system, along with effective order tracking management, is also very important. </li>
								
								<li>ust as trust in the seller is important, so is trust in the transaction-processing system. More than half of those surveyed cited transaction security as a primary concern. It means the platform through which the payment is made and processes needs to be absolutely secure. It also means the various touchpoints along the way to completing a transaction, all of which can create delays and additional costs if not handled properly –such as inspections, insurance, shipping etc.- need to operate transparently and smoothly.</li>
								
								<li>Commenting on the challenges faced by SMEs in terms of processing deals, a recent OECD report noted that barriers to access and use digital technologies usually are a result of a lack of high-quality and affordable infrastructure, a lack of trust in digital technologies and a shortage of skills9.</li>
								
								<li>In India, for example, the skills deficit includes a lack of expertise in peripheral activities, such as managing inventory, handling invoices and providing consumer insights. There are concerns and confidence issues with the technology and with the contractual and payment processes. </li>
								<li>From a technology standpoint, the main requirement for access to e-commerce in developing markets is one that is increasingly prevalent-a smartphone. Of course, that means there’s need for an e-commerce app and related solutions. Given the size of transactions and the modest scale of many SMEs, smartphone usage illustrates how mobile devices and greater internet access have resulted in far greater financial inclusion during the past decade.
								Just as with SME credit ratings, in the absence of alternatives, the markets and technology found a solution by growing trust and reliance on digital technology. Traditional brick-and mortar financial services have been leapfrogged.</li>

								Helping SMEs Get Started
								<h6 class="mb-1 mt-3"><strong>Eliminating risk </strong></h6>
								
								<li>The speed of technological change is both dramatic and empowering, yet for ATZ Cart - a data company –its primary tool for building trust is, in fact, data. Identifying the correlation, between removing risk and building trust was easy part; the challenge for ATZ Cart .com was to do it without exposure to substantial financial loss.</li>
								
								<li>In 2014, Shenzhen One-touch Enterprise Service Limited, a business process outsourcing company specializing in all facets of the cross- border transaction process, was acquired by ATZ Cart .com. It enabled ATZ Cart .com to combine its data and analytical capabilities with the cross-border business processing expertise of One Touch.</li>
								
								<li>The combination of data analytics on a vast scale with the business processing technology led to the creation of Trade Assurance, a system that eliminates risk for buyers and sellers and passes it on ATZ Cart b.com. Big data analysis allows  ATZ Cart .com to get a very close understanding of the parties involved, helping it to determine how much assurance to offer and in turn minimizes its own risk. SMEs can benefit from the business-processing expertise of One Touch to help address a shortage of skills and experience in certain aspects of cross-border trade.</li>
								
								<li>Within months of its launch, the service has attracted over 50,000 suppliers, reflecting both the scale of the market and the appetite for risk-aversion. There are now more than one million suppliers on ATZ Cart .com servicing more than 100 million buyers. These numbers continue to rise, with about 70% of suppliers using Trade Assurance.
								What is Trade Assurance?</li>
								
								<li>So what exactly is Trade Assurance? In short, it’s ATZ Cart ’s free order protection service. It’s also a trade facilitation program, where ATZ Cart  takes on the risk and protects both buyers and sellers. </li>
								
								<li>“ATZ Cart ’s Trade Assurance brought commercial ease and safety coupled with assurance on the quality of goods traded among the SMEs, increasing the confidence of those SMEs trading without any middlemen or agencies,” says Bhushan Patil, President of PayTM , India’s largest mobile e-commerce website. “The program helped genuine SME sellers and buyers find each other easily and offered data showcasing historical trade behavior and patterns, increasing the trade volumes which would otherwise have been smaller due to a lack of trust and information.” 
								Data garnered over time provides invaluable information for buyers and can showcase a supplier’s strengths in a numeric language that can be readily understood by all.</li>

								<h6 class="mb-1 mt-3"><strong>Trade Assurance facilitates logistics</strong> </h6>
								
								<li>Besides applying data and algorithms to help buyers and sellers find each other,
								“One Touch saves SMEs from the trouble of dealing with the complicated trading process,” says Mr. Ren, who oversees the ATZ Cart .com site operations.</li>
								
								<li>The value of Trade Assurance is evidenced by the fact that 60% of buyers using it have less than three years’ experience in foreign trade. It enables them to conduct business with both limited experience and peace of mind. SMEs tend to have a small staff – 70% of them have fewer than 10 employees- meaning they don’t have the resources to do all the related logistics and paperwork associated with international trade. Trade Assurance makes with international trade. Trade Assurance makes the time-consuming and expensive relationship building between buyers and sellers no longer necessary.</li>
								
								<li>Trade data collected since the launch of Trade Assurance shows that some 60% of buyers source infrequently or sporadically (some as little as once annually) and often with an individual order value of less than $1,000.0. So there’s a high amount of “turnover” and jumping around between suppliers. The tendency is for buyers to look for new suppliers each quarter, especially for trading companies and distributors. Trade Assurance makes switching suppliers easy and reduces associated costs and uncertainty around potential suppliers’ capabilities. </li>
								
								<h6 class="mb-1 mt-3" ><strong>Key features of Trade Assurance </strong></h6>
								
								<li>	Trade Assurance assesses every participating supplier and assigns them a ‘Trade Assurance Limit’ to cover buyers. The assessment is based on a supplier’s trade history and qualifications.<li>
								
								<li>The buyer makes a deposit or pre-payment to a designated supplier’s Citibank account. In the event of the supplier failing to meet the shipping time or pre-shipment product requirements agreed on in the contract, the buyer’s money is protected.</li>
								
								<li>In the event of a buyer’s claim for a refund not being settled within 15days, ATZ Cart .com will provide a full refund of the Trade Assurance Amount.</li>
								
								<li>Buyers get more visibility into sellers’ transactional history and credibility. Using the concept of “the power of influence,” buyers are encouraged to make their voices heard. If the supplier breaches any terms of a contract, ATZ Cart  will reduce the level of their Trade Assurance Limit. Such breaches have a direct monetary and therefore business impact. Bad supplier experiences can be shared through the ATZ Cart  feedback system. </li>
								
								<li>	Importantly, this includes protecting intellectual property- IP breaches or losses will hit the supplier’s credit score, so there’s a clear incentive to ensure IP is kept secure. </li>

