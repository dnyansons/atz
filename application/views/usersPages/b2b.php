								<h6 class="mb-1 mt-3"><strong>SMEs can focus on their core business</strong></h6>
								
								
								<li>The explosive growth in e-commerce worldwide over the past 10-15 years has created new business models and instigated fundamental change in the way international trade is conducted. The removal of barriers to such trade means that small business can now reach global markets. Scale is no longer a requirement; regional offices are no longer essential. Costly intermediaries can for the most part be jettisoned and global supply chains shortened.
								Saving time and money on sourcing, due diligence, insurance, logistics and so on enables SMEs to focus on what they do best: adding value through research, design, marketing, talent development, and more. It means a micro-multinational can now conduct global trade from a bedroom with a smartphone. This is the reality and it’s creating jobs. </li>
								
								
								<li>While noting that transactions in these developing markets are still in the process of moving from offline to online, meaning that orders tend to be large- scale, Mr. Ren adds, “After five or ten years, as the Internet matures in these markets, transactions may also turn from big-to-small-scale.”</li>

								<h6 class="mb-1 mt-3"><strong>Data-driven sourcing on the go</strong></h6>
								
								<li>Following these trends, the ATZ Cart .com mobile app team launched “ATZ Cart  Insights” in 2016. ATZ Cart  Insights is the first B2B mobile bug data platform that provides users with trade information, industry news and data-generated content. For example, before sourcing, users have the ability to view top-selling products and prices on retailer platforms such as eBay and Amazon by product category. They are then provided with links to supplier that offers similar products on ATZ Cart .com. In addition, ATZ Cart  Insights allows users to filter top-selling products by country, offering key market trends to entrepreneurs around the world. </li>
								
								<li>Mr. Ren notes: “To really make it easy to do business anywhere, we had to do more than provide our buyers with a mobile app platform to purchase products. So we rolled out an entire suite of big data tools that aim to inform buyers around the world of recent industry trends and pricing information. And that was phase one, we can still offer so much more.”</li>
								
								<h6 class="mb-1 mt-3"><strong>Unlocking Opportunity for SMEs</strong></h6>
								
								<li>ATZ Cart .com identified a key gap in sourcing trends between buying segments, finding that SMEs are often highly interested in matching with suppliers who posses very specific capabilities in terms of quantity and customization. The RFQ function and One-Click Sourcing Tool naturally filled the gap by harnessing the data from hundreds of thousands of transactions and applying a smart-matching system that makes finding suppliers based on detailed requirements simple-again leaving business to focus on their core competencies.</li>
								
								Economic and Social Benefits
								<h6 class="mb-1 mt-3"><strong>Innovative cross-cultural business</strong></h6>
								
								<li>The social and environmental impact of the democratization of credit ratings for SMEs is real. At a stroke, the credit rating systems has hugely simplified the sourcing and due diligence process. It’s an objective, data-based score eliminates any judgmental or “face” related issues. It’s cleaner, easier and a genuinely innovative cross-cultural solution for doing business internationally. It also has the added value of being demonstrably reliable.</li>
								
								<li>Trade Assurance also encourages netter work processes-work conditions, adherence to labour laws, environmental impact awareness and so forth. The distillation process that allows the best companies to rise to the top and become more visible leads to an overall upgrade in what they can produce-for example, a migration that cheap consumer electronics to high-tech circuit boards.</li>


