<script>
var interval = undefined;
$(document).ready(function () {
    interval = setInterval(getNext, 2000); // milliseconds
    $('#next1').on('click', getNext);
    $('#prev1').on('click', getPrev);
	
	$('#next2').on('click', getNextsecond);
    $('#prev2').on('click', getPrevsecond);
	
});

function getNext() {
     //alert("next");
    var $curr = $('.slideshow  img:visible'),
        $next = ($curr.next().length) ? $curr.next() : $('.slideshow img').first();

    transition($curr, $next);
}

function getPrev() {
	//alert("pre");
    var $curr = $('.slideshow  img:visible'),
        $next = ($curr.prev().length) ? $curr.prev() : $('.slideshow img').last();
    transition($curr, $next);
}

function transition($curr, $next) {
   clearInterval(interval);
    $next.css('z-index', 2).fadeIn(function () {
        $curr.hide().css('z-index', 0);
        $next.css('z-index', 1);
    });

}

function getNextsecond() {
     //alert("next");
    var $currimage = $('.slideshow2  img:visible'),
        $nextimg = ($currimage.next().length) ? $currimage.next() : $('.slideshow2 img').first();

    transition($currimage, $nextimg);
}

function getPrevsecond() {
	//alert("pre");
    var $currimage = $('.slideshow2  img:visible'),
        $nextimg = ($currimage.prev().length) ? $currimage.prev() : $('.slideshow2 img').last();
    transition($currimage, $nextimg);
}

function transition($currimage, $nextimg) {
   //clearInterval(interval);
    $nextimg.css('z-index', 2).fadeIn(function () {
        $currimage.hide().css('z-index', 0);
        $nextimg.css('z-index', 1);
    });

}


